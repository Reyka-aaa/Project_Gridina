# Скорость лодки в стоячей воде V км/ч, скорость течения реки U км/ч (U < V). Время движения лодки по озеру T1 ч, а по реке (против течения) T2 ч.
# Определить путь S, пройденный лодкой ( путь = время  скорость). Учесть, что при движении против течения скорость лодки уменьшается на величину скорости течения.
try:
    speed1 = int(input('Введите скорость лодки в стоячей воде км/ч: '))
    speed2 = int(input('Введите скорость течения реки км/ч: '))
    time1 = int(input('Введите время движения лодки по озеру ч: '))
    time2 = int(input('Введите время движения лодки по реке (против течения) ч: '))

    # Проверка что скорость лодки больше скорости течения
    if speed1 <= speed2:
        print("Ошибка: Скорость лодки должна быть больше скорости течения!")
    else:
        # Правильная формула: против течения скорость уменьшается
        S = (speed1 * time1) + ((speed1 - speed2) * time2)
        print("Путь, пройденный лодкой:", S, "км")

except ValueError:
    print("Ошибка: Введите корректные целые числа.")

