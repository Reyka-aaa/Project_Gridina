#1. Дано целое число A. Проверить истинность высказывания: «Число A является четным».
try:
    a = int(input("Введите  число А: "))
    if a % 2 == 0 :
        print('Число А является четным')
    else:
        print('Число А не является четным')
except ValueError:
    print('введите число')
